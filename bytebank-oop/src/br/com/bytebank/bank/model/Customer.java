package br.com.bytebank.bank.model;


public class Customer {
	private String name;
	private int pesel;
	
	public String getName() {
		return this.name;
	}
	
	public void setName(String newName) {
		this.name = newName;
	}
	
	public int getPesel() {
		return this.pesel;
	}
	
	public void setPesel(int newPesel) {
		this.pesel = newPesel;
	}
	
	
	
	
}
